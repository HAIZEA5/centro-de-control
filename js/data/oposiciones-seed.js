/**
 * Importa el seed al localStorage, sin sobreescribir entradas existentes.
 * Compara por nombre normalizado. Ejecuta al cargar la página.
 */
function opos_applySeed() {
  const vKey = 'opos_seed_v';
  const vActual = parseInt(localStorage.getItem(vKey) || '0');
  if (vActual >= OPOS_SEED_VERSION) return;

  const actuales = Store.get('opos_convocatorias', []);
  const norm = s => (s || '').toLowerCase().replace(/[^a-záéíóúüñ0-9]/g, '');

  const isSameConv = (a, b) => {
    const ca = norm(a), cb = norm(b);
    if (ca === cb) return true;
    const minLen = Math.min(ca.length, cb.length);
    const shared = [...cb].filter((c, i) => ca[i] === c).length;
    return shared / minLen > 0.85;
  };

  let añadidas = 0, actualizadas = 0;
  OPOS_SEED.forEach(nueva => {
    const idx = actuales.findIndex(e => isSameConv(e.convocatoria || '', nueva.convocatoria));
    if (idx === -1) {
      actuales.push(nueva);
      añadidas++;
    } else {
      // Actualiza solo campos del seed que no haya modificado el usuario manualmente
      // (campos estructurales: fase, grupo, estado, fechas, urls, notas, docs)
      const seedFields = ['fase','grupo','estado','fecha_examen','hora_examen','fecha_apertura','fecha_fin_inscr',
        'url_boe','url_bases','url_temario','url_extra1','url_extra2','notas',
        'doc_solicitud','doc_dni','doc_titulacion','doc_euskera','doc_cv','doc_meritos','doc_discap',
        'doc_extra1_nombre','doc_extra1','doc_extra2_nombre','doc_extra2','doc_extra3_nombre','doc_extra3',
        'meritos_calc'];
      seedFields.forEach(f => { if (nueva[f] !== undefined) actuales[idx][f] = nueva[f]; });
      actualizadas++;
    }
  });

  Store.set('opos_convocatorias', actuales);
  localStorage.setItem(vKey, String(OPOS_SEED_VERSION));
  console.info(`[Oposiciones] Seed v${OPOS_SEED_VERSION}: ${añadidas} añadidas, ${actualizadas} actualizadas.`);
}
